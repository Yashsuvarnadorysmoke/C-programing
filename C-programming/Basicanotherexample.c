#include <stdio.h>
// basic syntax Example 
// print string 
// print integer
// print float
// print double
// print character
int main()
{
    
    printf("Hello World\n");
    printf("Number: %d\n", 100);
    printf("Float: %f\n", 100.0);
    printf("Double: %lf\n", 100.0);
    printf("Character: %c\n", 'A');
    
    return 0;
}